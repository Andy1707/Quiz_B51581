    public void generarFicha() {
        
        this.jt_Ficha.setText("");
        int posLetra= (int) Math.round(Math.random()*26) ;
        int numAleatorio= (int) (Math.random()*(99-10+1)+10);
        this.jt_Ficha.setText(abecedario[posLetra]+numAleatorio);
    }
    public String[] devolverInformacion() {
        
        String arreglo[]= new String[5];
        
        arreglo[0]=this.jt_Ficha.getText();
        arreglo[1]=this.jt_Nombre.getText().trim();
        arreglo[2]=this.jt_Edad.getText().trim();
        arreglo[3]=this.servicio;
        arreglo[4]=this.prioridad;
        
        return arreglo;
    }
